package Datos;

public class Ventana {
	private double alto;	
	private double ancho;	
	
	public Ventana(double alto, double ancho) {		
		this.alto = alto;
		this.ancho = ancho;
	}
		
	public double getAlto() {
		return alto;
	}
	
	public void setAlto(double alto) {
		this.alto = alto;
	}
	
	public double getAncho() {
		return ancho;
	}
	
	public void setAncho(double ancho) {
		this.ancho = ancho;
	}

}


